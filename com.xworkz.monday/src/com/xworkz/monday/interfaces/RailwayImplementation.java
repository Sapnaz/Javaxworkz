package com.xworkz.monday.interfaces;

public class RailwayImplementation implements KSR {

	@Override
	public void carryValidTicket() {
		System.out.println("Passengers must have a valid ticket to board the train");

	}

	@Override
	public void avoidRestrictedAreas() {
		System.out.println("Do not travel in restricted areas such as engine or freight compartments");

	}

	@Override
	public void maintainCleanliness() {
		System.out.println("Maintain cleanliness in railway premises and trains");

	}

	@Override
	public void followBoardingSafety() {
		System.out.println("Follow safety guidelines while boarding and exiting the train");

	}

	@Override
	public void noSmoking() {
		System.out.println("No smoking in stations or trains");
	}

	@Override
	public void avoidMisuseOfEmergencyChain() {
		System.out.println("Do not pull the emergency chain without a valid reason");

	}

	@Override
	public void respectReservedSeating() {
		System.out.println("Respect reserved seating for the disabled, elderly, and women");
	}

	@Override
	public void useDesignatedCrossings() {
		System.out.println("Do not cross railway tracks, use footbridges or designated crossings");

	}

	@Override
	public void followQueueDiscipline() {
		System.out.println("Follow proper queue behavior while buying tickets or boarding the train");
	}

	@Override
	public void followRailwayStaffInstructions() {
		System.out.println("Obey the instructions of railway staff and law enforcement");

	}

	@Override
	public void avoidCarryingProhibitedItems() {
		System.out.println("Refrain from carrying prohibited items (explosives, dangerous goods");

	}

	@Override
	public void avoidDangerousTravel() {
		System.out.println("Be cautious when traveling on the roof or hanging outside the train");
	}

	@Override
	public void followTrainTimings() {
		System.out.println("Follow train timings and announcements");

	}

	@Override
	public void respectRailwayProperty() {
		System.out.println("Avoid causing damage or vandalizing railway property");

	}

	@Override
	public void respectOtherPassengers() {
		System.out.println("Respect the comfort of fellow passengers");

	}

	@Override
	public void display() {
		System.out.println("running display");

	}

}
